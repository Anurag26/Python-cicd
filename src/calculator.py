import unittest

def calculator(operation, num1, num2):
    if operation == "+":
        return num1+num2
    elif operation == "-":
        return num1-num2
    elif operation == "*":
        return num1*num2
    elif operation == "/":
        return num1/num2
    elif operation == "%":
        return num1%num2
    
    


